    private void setFormatPrice() {
        NumberFormatter formatter = new NumberFormatter();
        formatter.setValueClass(BigDecimal.class);
        formatter.setMinimum(BigDecimal.ZERO);
        formatter.setFormat(NumberFormat.getInstance());
        formatter.setAllowsInvalid(false);
        DefaultFormatterFactory factory = new DefaultFormatterFactory(formatter);
        ft_price.setFormatterFactory(factory);
    }
    
    private void save(int productId, int quantity, BigDecimal price) {
        if(!this.validedForm(quantity)){
            return;
        }
        int state = movementFrame.movementService.input(productId, quantity, price);
        switch (state) {
            case 0 -> {
                JOptionPane.showMessageDialog(rootPane, "Agregado correctamente");
                this.movementFrame.refreshData();
            }
            case 2 -> JOptionPane.showMessageDialog(rootPane, "El producto no tiene suficiente stock");
            default -> JOptionPane.showMessageDialog(rootPane, "No se pudo guardar");
        }
    }
    
    private boolean validedForm(int quantity){
        if(quantity <= 0) {
            JOptionPane.showMessageDialog(rootPane, "Ingrese una cantidad vÃ¡lida");
            return false;
        }
        return true;
    }
    
    private void loadProducts() {
        for(Product product : this.movementFrame.movementService.productService.getProducts()) {
            cb_product.addItem(product);
        }
    }

