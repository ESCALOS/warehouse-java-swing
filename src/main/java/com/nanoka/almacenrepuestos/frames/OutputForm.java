    private void save(int productId, int quantity, int available) {
        if(!this.validedForm(quantity, available)){
            return;
        }
        int state = movementFrame.movementService.output(productId, quantity);
        switch (state) {
            case 0 -> {
                JOptionPane.showMessageDialog(rootPane, "Agregado correctamente");
                sp_quantity.setValue(1);
                Product product = (Product) cb_product.getSelectedItem();
                int stock = product.getStock() - quantity;
                product.setStock(stock);
                txt_available.setText(String.valueOf(stock));
                this.movementFrame.refreshData();
            }
            case 2 -> JOptionPane.showMessageDialog(rootPane, "No hay suficiente stock");
            default -> JOptionPane.showMessageDialog(rootPane, "No se pudo guardar");
        }
    }
    
    private boolean validedForm(int quantity,int available){
        if(quantity <= 0) {
            JOptionPane.showMessageDialog(rootPane, "Ingrese una cantidad vÃ¡lida");
            return false;
        }
        
        if(quantity > available) {
            JOptionPane.showMessageDialog(rootPane, "No hay suficiente stock");
            return false;
        }
        return true;
    }
    
    private void loadProducts() {
        for(Product product : this.movementFrame.movementService.productService.getProducts()) {
            cb_product.addItem(product);
        }
    }

